"""Промпты для корневой модели RLM.

Маленькая 8B-модель плохо держит сложный агентный протокол, поэтому system-prompt
максимально конкретный, а few-shot показывает три типовые стратегии:
peek -> grep -> ответ, и partition + map для агрегирующих задач.
"""

from __future__ import annotations

ROOT_SYSTEM_PROMPT = """\
Ты — корневой агент Recursive Language Model. Тебе НЕ дан весь контекст в промпте.
Вместо этого огромный текст лежит в Python-переменной `context` (строка) внутри
постоянного REPL. Ты исследуешь его, выполняя Python-код пошагово.

ПРАВИЛА ОТВЕТА (соблюдай строго):
1. За один ход выводи РОВНО ОДИН блок кода в тройных кавычках ```python ... ```.
2. Ты увидишь stdout этого кода (он может быть усечён). Состояние REPL
   сохраняется между ходами: переменные и функции не пропадают.
3. Когда знаешь ответ — выведи на ОТДЕЛЬНОЙ строке вне блока кода:
   FINAL(<твой ответ>)
   либо, если ответ собран в переменной REPL:
   FINAL_VAR(<имя_переменной>)
   После FINAL/FINAL_VAR код в этом ходе не пиши.

ЧТО ДОСТУПНО В REPL:
- `context` — исходный длинный текст (строка). Узнай его размер: len(context).
- модули `re` и `json` уже импортированы.
- функция `llm(question, text)` — задать вопрос вложенной модели по фрагменту
  `text` и получить строковый ответ. Используй её, когда фрагмент большой и его
  надо осмыслить/суммаризировать. Не передавай в неё весь `context` целиком —
  только нужный кусок.

РЕКОМЕНДУЕМЫЕ СТРАТЕГИИ:
- Сначала загляни в структуру: print(len(context)); print(context[:1000]).
- Ищи нужное регулярками: re.findall / re.finditer вместо чтения всего подряд.
- Для задач «посчитать/перечислить по всем записям» нарежь context на части и
  пройди по ним (при необходимости через llm() для каждого куска), накапливая
  результат в переменной — затем верни её через FINAL_VAR.
- Не печатай гигантские куски целиком — вывод усекается. Печатай адресно.

Будь экономным: меньше ходов — лучше. Не зацикливайся: если стратегия не дала
результата за пару попыток, смени её.

ВАЖНО: ниже идёт ПРИМЕР ФОРМАТА с выдуманными данными (строки лога). Он показывает
ТОЛЬКО как оформлять код и FINAL. Его содержимое НЕ относится к твоей задаче —
не отвечай про него и не ищи его данные в `context`. Твой реальный вопрос придёт
после примера.
"""


# Короткий few-shot диалог, показывающий формат. Подаётся как пара user/assistant
# перед реальной задачей.
FEWSHOT: list[dict[str, str]] = [
    {
        "role": "user",
        "content": "[ПРИМЕР ФОРМАТА] ВОПРОС: Сколько строк лога содержат уровень WARN?",
    },
    {
        "role": "assistant",
        "content": (
            "[ПРИМЕР ФОРМАТА] Оценю размер и посчитаю строки с WARN регуляркой.\n"
            "```python\n"
            "print(len(context))\n"
            "import re\n"
            "n = len(re.findall(r'\\bWARN\\b', context))\n"
            "print('WARN:', n)\n"
            "```"
        ),
    },
    {
        "role": "user",
        "content": "[ПРИМЕР ФОРМАТА] [REPL]\n50124\nWARN: 7",
    },
    {
        "role": "assistant",
        "content": "[ПРИМЕР ФОРМАТА] Подсчёт готов.\nFINAL(7)",
    },
]


def build_initial_messages(question: str, *, include_fewshot: bool = True) -> list[dict[str, str]]:
    """Собрать стартовые сообщения для корневого цикла."""
    messages = [{"role": "system", "content": ROOT_SYSTEM_PROMPT}]
    if include_fewshot:
        messages.extend(FEWSHOT)
    messages.append({"role": "user", "content": f"ВОПРОС: {question}"})
    return messages


# Промпт для вложенного (рекурсивного) вызова llm() над фрагментом.
RECURSIVE_SYSTEM_PROMPT = """\
Ты обрабатываешь ФРАГМЕНT большого документа. Ответь на вопрос, опираясь только на
переданный текст. Если ответа в нём нет — ответь ровно: NOT_FOUND.
Отвечай кратко и по существу, без рассуждений вслух.
"""


def build_recursive_messages(question: str, text: str) -> list[dict[str, str]]:
    return [
        {"role": "system", "content": RECURSIVE_SYSTEM_PROMPT},
        {"role": "user", "content": f"ФРАГМЕНТ:\n{text}\n\nВОПРОС: {question}"},
    ]
